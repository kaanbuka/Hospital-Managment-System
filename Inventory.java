    private void txtcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcodeKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
        String dcode = txtcode.getText();
        Connection con = ConnectionProvider.getCon();
        Statement st = null;
        ResultSet rs = null;

        try {
            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM item WHERE itemid = '" + dcode + "'");
            
            
            if (rs.next() == false) {
                JOptionPane.showMessageDialog(this, "Drug Not Found!");
            } else {
                
                String dname = rs.getString("itemname");
                txtname.setText(dname.trim());
                txtqty.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            
            try {
                if (rs != null) rs.close();
                if (st != null) st.close();
                if (con != null) con.close();
            } catch (SQLException ex) {
                Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    }//GEN-LAST:event_txtcodeKeyPressed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String pcode = txtcode.getText();
        Connection con = ConnectionProvider.getCon();
        try {
            pst = con.prepareStatement("SELECT * FROM item WHERE itemid='"+pcode+"'");
            rs = pst.executeQuery();
            
            while(rs.next()) {
                int currentqty;
                int sellprice;
                int qty;
                
                currentqty = rs.getInt("qty");
                sellprice = rs.getInt("sellprice");
                
                qty = Integer.parseInt(txtqty.getValue().toString());
                int tot = sellprice * qty;
                
                
                if(qty >= currentqty) {
                    JOptionPane.showMessageDialog(this, "Available Item " + currentqty);
                }
                else {
                    DefaultTableModel DF = (DefaultTableModel)jTable1.getModel();
                    DF.addRow(new Object[] {
                    lblpid.getText(),
                    txtcode.getText(),
                    txtname.getText(),
                    txtqty.getValue().toString(),
                    sellprice,                  
                    tot,  
                });
                    
                    int sum = 0;
                    for(int i=0; i<jTable1.getRowCount(); i++) {
                        sum = sum + Integer.parseInt(jTable1.getValueAt(i, 5).toString());
                    }
                    
                    txtcost.setText(Integer.toString(sum));
                    txtcode.setText("");
                    txtname.setText("");
                    txtqty.setValue(0);
                    txtcode.requestFocus();
                    
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

