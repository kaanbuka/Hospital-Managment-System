    public void sales() {
        DateTimeFormatter daa = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime now = LocalDateTime.now();
        
        String date = daa.format(now);
        
        
        String subtot = txtcost.getText();
        String pay = txtpay.getText();
        String balance = txtbal.getText();
        
        int lastinsertid = 0;
        Connection con = ConnectionProvider.getCon();
        
        
        try {
            String query = "insert into sales(_date, subtotal, pay, balance) values(?,?,?,?)";
            pst = con.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, date);
            pst.setString(2, subtot);
            pst.setString(3, pay);
            pst.setString(4, balance);
            
            
            pst.executeUpdate();
            
            rs = pst.getGeneratedKeys();
            if(rs.next()) {
                lastinsertid = rs.getInt(1);
            }
            
            
            int rows = jTable1.getColumnCount();
            String query1 = "insert into sales_product(sales_id, prod_id, sellprice, qty, total) values(?,?,?,?,?)";
            pst = con.prepareStatement(query1);
            String pres_id;
            String item_id;
            String item_name;
            String price;
            String qty;
            int total;
            
            
            for(int i=0; i<jTable1.getRowCount(); i++) {
                pres_id = (String)jTable1.getValueAt(i, 0).toString();
                item_id = (String)jTable1.getValueAt(i, 1).toString();
                qty = jTable1.getValueAt(i, 3).toString();
                int qty1 = Integer.parseInt(qty);
                price = (String)jTable1.getValueAt(i, 4).toString();
                total = (int)jTable1.getValueAt(i, 5);
                
                
                pst.setInt(1, lastinsertid);
                pst.setString(2, item_id);
                pst.setInt(3, qty1);
                pst.setString(4, price);
                pst.setInt(5, total);
                
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Record Added!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

