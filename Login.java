    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = txtusername.getText();
        String password = txtpass.getText();
        String utype = txtutype.getSelectedItem().toString();
        
        
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
    
            String query = "SELECT * FROM user WHERE username='" + username + "' AND _password='" + password + "' AND utype='" + utype + "'";
            ResultSet rs = st.executeQuery(query);
    
            if (rs.next()) {
                int userid = rs.getInt("id");
                
                //JOptionPane.showMessageDialog(null, "Ã‡ekilen ID: " + userid);
                this.setVisible(false);
                new Main(userid, username, utype).setVisible(true);
                
            } else {
             JOptionPane.showMessageDialog(null, "Username or Password doesn't match!");
             txtusername.setText("");
             txtpass.setText("");
             txtutype.setSelectedIndex(-1);
             txtusername.requestFocus();
            }
    
            rs.close();
            st.close();
            con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Please enter data in correct form! Error: " + e.getMessage());
}

    }//GEN-LAST:event_jButton1ActionPerformed

